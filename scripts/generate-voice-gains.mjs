import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, relative, sep } from 'node:path';

const voiceDir = join(process.cwd(), 'public', 'voice');
const outputPath = join(process.cwd(), 'src', 'generatedVoiceGains.ts');
const targetRms = dbToLinear(-18.5);
const peakCeiling = dbToLinear(-1.5);
const activeSampleThreshold = dbToLinear(-50);
const minGain = 0.6;
const maxGain = 1.25;

function dbToLinear(db) {
  return 10 ** (db / 20);
}

function walk(dir) {
  return readdirSync(dir, { withFileTypes: true }).flatMap(entry => {
    const path = join(dir, entry.name);
    return entry.isDirectory() ? walk(path) : [path];
  });
}

function readWavPcm16MonoOrStereo(path) {
  const buffer = readFileSync(path);
  if (buffer.toString('ascii', 0, 4) !== 'RIFF' || buffer.toString('ascii', 8, 12) !== 'WAVE') {
    throw new Error(`${path} is not a RIFF/WAVE file`);
  }

  let offset = 12;
  let format = null;
  let data = null;
  while (offset + 8 <= buffer.length) {
    const id = buffer.toString('ascii', offset, offset + 4);
    const size = buffer.readUInt32LE(offset + 4);
    const start = offset + 8;
    if (id === 'fmt ') {
      format = {
        audioFormat: buffer.readUInt16LE(start),
        channels: buffer.readUInt16LE(start + 2),
        sampleRate: buffer.readUInt32LE(start + 4),
        bitsPerSample: buffer.readUInt16LE(start + 14),
      };
    } else if (id === 'data') {
      data = buffer.subarray(start, start + size);
    }
    offset = start + size + (size % 2);
  }

  if (!format || !data) throw new Error(`${path} has no fmt/data chunk`);
  if (format.audioFormat !== 1 || format.bitsPerSample !== 16) {
    throw new Error(`${path} must be 16-bit PCM`);
  }

  return { data, sampleRate: format.sampleRate, channels: format.channels };
}

function analyzeWav(path) {
  const { data } = readWavPcm16MonoOrStereo(path);
  let peak = 0;
  let activeSquareSum = 0;
  let activeCount = 0;

  for (let offset = 0; offset + 1 < data.length; offset += 2) {
    const value = Math.abs(data.readInt16LE(offset)) / 32768;
    if (value > peak) peak = value;
    if (value >= activeSampleThreshold) {
      activeSquareSum += value * value;
      activeCount += 1;
    }
  }

  const activeRms = activeCount > 0 ? Math.sqrt(activeSquareSum / activeCount) : 0;
  const rmsGain = activeRms > 0 ? targetRms / activeRms : 1;
  const peakGain = peak > 0 ? peakCeiling / peak : 1;
  const gain = Math.max(minGain, Math.min(maxGain, rmsGain, peakGain));

  return {
    gain: Math.round(gain * 100) / 100,
    peak,
    activeRms,
  };
}

const entries = walk(voiceDir)
  .filter(path => path.toLowerCase().endsWith('.wav'))
  .map(path => {
    const src = `/${relative(join(process.cwd(), 'public'), path).split(sep).join('/')}`;
    return { src, ...analyzeWav(path) };
  })
  .sort((a, b) => a.src.localeCompare(b.src));

const generatedAt = new Date().toISOString();
const lines = [
  '// This file is generated by scripts/generate-voice-gains.mjs.',
  '// It keeps VOICE playback roughly even without rewriting source audio files.',
  `// Generated at ${generatedAt}.`,
  'export const GENERATED_VOICE_AUDIO_GAINS: Record<string, number> = {',
  ...entries.map(entry => `  '${entry.src}': ${entry.gain},`),
  '};',
  '',
];

writeFileSync(outputPath, lines.join('\n'));
console.log(`Generated ${entries.length} voice gain entries -> ${outputPath}`);
